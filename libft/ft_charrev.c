#include "libft.h"

char		*ft_charrev(char *s)
{
	int		i;
	int		q;
	int		m;
	char	a;

	if (!s)
		return (NULL);
	i = 0;
	q = 0;
	m = 0;
	while (s[q++])
		i = q - 1;
	q = q / 2;
	while (q-- > 0)
	{
		a = s[m++];
		s[m - 1] = s[i--];
		s[i + 1] = a;
	}
	return (s);
}
