#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t	i;

	i = 0;
	while (src[i] && i < len)
	{
		dst[i] = src[i];
		i++;
	}
	while (i < len)
	{
		dst[i] = '\0';
		i++;
	}
	if (ft_strlen(src) > len)
	{
		if (ft_strlen(src) >= ft_strlen(dst))
			dst[len] = '\0';
		else
			dst[ft_strlen(dst)] = '\0';
	}
	return (dst);
}
