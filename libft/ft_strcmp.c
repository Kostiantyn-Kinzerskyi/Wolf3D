int	ft_strcmp(const char *s1, const char *s2)
{
	int				i;
	unsigned char	q;
	unsigned char	w;

	if (!s1 || !s2)
		return (0);
	i = 0;
	while (s1[i] == s2[i] && s1[i] && s2[i])
		i++;
	q = s1[i];
	w = s2[i];
	return (q - w);
}
